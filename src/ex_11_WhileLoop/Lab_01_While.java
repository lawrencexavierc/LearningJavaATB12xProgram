package ex_11_WhileLoop;

public class Lab_01_While {
    public static void main(String[] args) {
       int i=0;
        while(i<10)
        {
            System.out.println(i);
            i++;
        }
        System.out.println("-------------------------");
        for (i=0;i<10;i++)
        {
            System.out.println(i);
        }
    }
}
